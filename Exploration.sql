-- magnitude analysis , understand importance of different cata by compare vlaues by cata
-- find total costomers by countries
SELECT country, COUNT(DISTINCT customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY country
ORDER BY total_customers DESC
-- find total customers by gender
SELECT gender, COUNT(DISTINCT customer_key) AS total_customers
FROM gold.dim_customers
GROUP BY gender
ORDER BY total_customers DESC
-- total products by cata
SELECT category, COUNT(DISTINCT product_key) AS total_products
FROM gold.dim_products
GROUP BY category
ORDER BY total_products DESC
--avergae cost in each cata
SELECT category, AVG(cost) AS average_price
FROM gold.dim_products
GROUP BY category
ORDER BY average_price DESC
-- total revenue generated by each cata
SELECT p.category, SUM(f.sales_amount) AS total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p ON f.product_key = p.product_key
GROUP BY p.category
ORDER BY total_revenue DESC
-- total recenye generated by each customer
SELECT c.customer_key, c.first_name, c.last_name, SUM(f.sales_amount) AS total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c ON f.customer_key = c.customer_key
GROUP BY c.customer_key, c.first_name, c.last_name
ORDER BY total_revenue DESC
-- distribution of sold items acrose countries
SELECT c.country, SUM(f.quantity) AS total_items_sold
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c ON f.customer_key = c.customer_key
GROUP BY  c.country
ORDER BY total_items_sold DESC

