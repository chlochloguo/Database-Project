-- Dimension exploration 
-- explore all countres customers come from
SELECT DISTINCT country
FROM gold.dim_customers
-- explore all categories
SELECT DISTINCT category, subcategory, product_name
FROM gold.dim_products
ORDER BY category, subcategory, product_name

-- Date exploration 
-- find date of first and last order, how many years of sales are avaliable
SELECT MIN(order_date) AS first_order_date, MAX(order_date) AS last_order_date,
       DATEDIFF(YEAR, MIN(order_date), MAX(order_date)) AS years_of_sales
FROM gold.fact_sales
-- find youngest and oldest customers
SELECT MIN(birthdate) AS oldest_customer,
DATEDIFF(year, MIN(birthdate), GETDATE()) AS oldest_age,
 MAX(birthdate) AS youngest_customer,
DATEDIFF(year, MAX(birthdate), GETDATE()) AS youngest_age
FROM gold.dim_customers

-- Measure exploration 
-- find total sales
SELECT SUM(sales_amount) AS total_sales
FROM gold.fact_sales
-- find how many items sold
SELECT SUM(quantity) AS total_items_sold
FROM gold.fact_sales
-- find average sales 
SELECT AVG(price) AS average_price
FROM gold.fact_sales
-- total number of orders
SELECT COUNT(DISTINCT order_number) AS total_orders
FROM gold.fact_sales
-- total number of products
SELECT COUNT(DISTINCT product_key) AS total_products
FROM gold.dim_products
-- total number of customers
SELECT COUNT(DISTINCT customer_key) AS total_customers
FROM gold.dim_customers

